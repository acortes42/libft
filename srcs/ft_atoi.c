#include "../libft.h"

int		ft_atoi(const char *j)
{
	long int i[3];

	i[0] = 0;
	i[2] = 0;
	i[1] = 1;
	while (j[i[0]] == 32 || (j[i[0]] >= 9 && j[i[0]] <= 13))
		i[0]++;
	if (j[i[0]] < '0' && j[i[0]] > '9' && j[i[0]] != '-' && j[i[0]] != '+')
		return (0);
	if (j[i[0]] == '-' || j[i[0]] == '+')
	{
		if (j[i[0]] == '-')
			i[1] = (i[1] * (-1));
		i[0]++;
	}
	while (('9' >= j[i[0]]) && (j[i[0]] >= '0'))
	{
		i[2] = (i[2] * 10) + (j[i[0]] - '0');
		i[0]++;
		if (i[2] * i[1] > 2147483647)
			return (-1);
		else if (i[2] * i[1] < -2147483648)
			return (0);
	}
	return (i[2] * i[1]);
}
